def string(word):
  for letter in word:
    if letter == 'a' or letter == 'A':
        print('a')
    if letter == 'e' or letter == 'E':
        print('e')
    if letter == 'i' or letter == 'I':
        print('i')
    if letter == 'o' or letter == 'O':
        print('o')
    if letter == 'u' or letter == 'U':
        print('u')
print("Vowels:")
string()
